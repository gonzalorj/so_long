#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*dest;
	size_t	n;
	size_t	i;

	i = 0;
	n = ft_strlen(src);
	dest = (char *)malloc(n + 1);
	if (!dest)
		return (NULL);
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
